-- Took a back up of HC_WEB_DB and added the GUAL2012 fields using external spatial processing software.
-- Restored database with GUAL2012 updates as HC_WEB_DB_U.
-- Added three new columns to HC_WEB_DB.dbo.CELL_VALUES: GAUL_2012_0, GAUL_2012_1, GAUL_2012_2 as nvarchar(100)
-- Ran the following scripts.

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_0 = (SELECT TOP 1 GAUL_2012_0 FROM HC_WEB_DB_DEV.dbo.CELL_VALUES
WHERE HC_WEB_DB.dbo.CELL_VALUES.CELL5M = HC_WEB_DB_DEV.DBO.CELL_VALUES.CELL5M)

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_1 = (SELECT TOP 1 GAUL_2012_1 FROM HC_WEB_DB_DEV.dbo.CELL_VALUES
WHERE HC_WEB_DB.dbo.CELL_VALUES.CELL5M = HC_WEB_DB_DEV.DBO.CELL_VALUES.CELL5M)

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_2 = (SELECT TOP 1 GAUL_2012_2 FROM HC_WEB_DB_DEV.dbo.CELL_VALUES
WHERE HC_WEB_DB.dbo.CELL_VALUES.CELL5M = HC_WEB_DB_DEV.DBO.CELL_VALUES.CELL5M)

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_0 = GAUL_2008_0
WHERE GAUL_2012_0 IS NULL OR GAUL_2012_0 = ''

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_1 = GAUL_2008_1
WHERE GAUL_2012_1 IS NULL OR GAUL_2012_1 = ''

UPDATE HC_WEB_DB.DBO.CELL_VALUES 
SET GAUL_2012_2 = GAUL_2008_2
WHERE GAUL_2012_2 IS NULL OR GAUL_2012_2 = ''

SELECT GAUL_2012_0, GAUL_2008_0, GAUL_2012_1, GAUL_2008_1, GAUL_2012_2, GAUL_2008_2
FROM HC_WEB_DB.dbo.CELL_VALUES